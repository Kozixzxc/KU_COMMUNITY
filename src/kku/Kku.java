package kku;

public class Kku {
	private int kkuID;
	private String kkuTitle;
	private String userID;
	private String kkuDate;
	private String kkuContent;
	private int kkuAvailable;
	
	public int getKkuID() {
		return kkuID;
	}
	public void setKkuID(int kkuID) {
		this.kkuID = kkuID;
	}
	public String getKkuTitle() {
		return kkuTitle;
	}
	public void setKkuTitle(String kkuTitle) {
		this.kkuTitle = kkuTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getKkuDate() {
		return kkuDate;
	}
	public void setKkuDate(String kkuDate) {
		this.kkuDate = kkuDate;
	}
	public String getKkuContent() {
		return kkuContent;
	}
	public void setKkuContent(String kkuContent) {
		this.kkuContent = kkuContent;
	}
	public int getKkuAvailable() {
		return kkuAvailable;
	}
	public void setKkuAvailable(int kkuAvailable) {
		this.kkuAvailable = kkuAvailable;
	}
	
}
